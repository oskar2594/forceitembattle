    public static List<Pair<GameDifficulty, Material>> COMBLIST_EASY = addList(new ArrayList<>(), EASY, GameDifficulty.EASY);
    public static List<Pair<GameDifficulty, Material>> COMBLIST_MEDIUM = addList((List<Pair<GameDifficulty, Material>>) Utils.cloneList(COMBLIST_EASY), MEDIUM, GameDifficulty.MEDIUM);
    public static List<Pair<GameDifficulty, Material>> COMBLIST_HARD = addList((List<Pair<GameDifficulty, Material>>) Utils.cloneList(COMBLIST_MEDIUM), HARD, GameDifficulty.HARD);


    public static Pair<GameDifficulty, Material> getRandomMaterial(GameDifficulty difficulty, LinkedList<Collectable> exclude) {
        List<Pair<GameDifficulty, Material>> list = new ArrayList<>();
        switch (difficulty) {
            case EASY:
                list = COMBLIST_EASY;
                break;
            case MEDIUM:
                list = COMBLIST_MEDIUM;
                break;
            case HARD:
                list = COMBLIST_HARD;
                break;
        }
        list.removeIf(pair -> {
            for (Collectable collectable : exclude) {
                if (collectable.getMaterial().name().equals(pair.getRight().name())) {
                    return true;
                }
            }
            return false;
        });
        return list.get(new Random().nextInt(list.size()));
    }

    private static List<Pair<GameDifficulty, Material>> addList(List<Pair<GameDifficulty, Material>> base, List<Material> add, GameDifficulty difficulty) {
        List<Pair<GameDifficulty, Material>> materials = base;
        add.forEach(material -> {
            Pair<GameDifficulty, Material> pair = new Pair<>(difficulty, material);
            materials.add(new Pair<GameDifficulty, Material>(difficulty, material));
        });
        return materials;
    }
